#include <stdio.h>
//有符号，无符号，整型提升
int main()
{
	char a = -1;
	//10000000000000000000000000000001-源
	//11111111111111111111111111111110-反
	//11111111111111111111111111111111-补
	//11111111
	//11111111111111111111111111111111
	signed char b = -1;
	//11111111111111111111111111111111
	unsigned char c = -1;
	//00000000000000000000000011111111
	printf("a=%d\nb=\nc=%d", a, b, c);
	return 0;
}
