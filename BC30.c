//描述
//给定秒数 seconds ，把秒转化成小时、分钟和秒。
//数据范围：  
//0<seconds<100000000 
//输入描述：
//一行，包括一个整数，即给定的秒数。
//输出描述：
//一行，包含三个整数，依次为输入整数对应的小时数、分钟数和秒数（可能为零），中间用一个空格隔开。

#include <stdio.h>

int main() {
    int i, h, m, s;
    scanf("%d", &i);
    h = i / 3600;
    m = (i - 3600 * h) / 60;
    s = (i - 3600 * h) % 60;
    printf("%d %d %d", h, m, s);
    return 0;
}
