//从键盘输入一个整数，请你判断这个整数能被 2 3 7 中哪几个数整除，并按升序输出。如果不能被 2 3 7 任意一个数整除则输出 n。
//输入描述：
//输入一个整数
//输出描述：
//输出能被 2 3 7 哪几个数整除，并按升序输出。

#include <stdio.h>

int main() {
    int n;
    scanf("%d", &n);
    if (n % 2 != 0 && n % 3 != 0 && n % 7 != 0) {
        printf("n");
    }
    if (n % 2 == 0) {
        n = n / 2;
        printf("2 ");
    }
    if (n % 3 == 0) {
        n = n / 3;
        printf("3 ");
    }
    if (n % 7 == 0) {
        n = n / 7;
        printf("7 ");
    }
    return 0;
}
