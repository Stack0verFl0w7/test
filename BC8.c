//尝试用键盘读入一个字符，然后在屏幕上显示一个用这个字符填充的对角线长5个字符，倾斜放置的菱形。
//输入描述：
//输入一个char类型字符
//输出描述：
//输出一个用这个字符填充的对角线长5个字符，倾斜放置的菱形

#include <stdio.h>

int main() {
	char a;
	int i;
	int j;
	scanf("%c", &a);
	for (i = 1; i <= 5; i++) {
		for (j = 1; j <= 5; j++) {
			if (i == 3 || j == 3 || (i == 2 && ((j == 4) || (j == 2))) || (i == 4 && ((j == 2) || (j == 4)))) {
				printf("%c", a);
			}
			else {
				printf(" ");
			}
		}
		printf("\n");
	}
	return 0;
}
