{
	char a = -128;
	//10000000000000000000000010000000
	//11111111111111111111111101111111
	//11111111111111111111111110000000
	//10000000
	//11111111111111111111111110000000
	printf("%u\n", a);
	//%d-打印十进制的有符号数字
	//%u-打印十进制的无符号数字
	return 0;
}
